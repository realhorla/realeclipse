// Function to check if a word belongs to a specific category
export function isWordInCategory(word, categoryName) {
  const normalizedWord = word.toLowerCase().trim();
  const category = categoryWords[categoryName];
  
  if (!category) {
    return false;
  }
  
  return category.includes(normalizedWord);
}

// Function to get all valid words for a category
export function getCategoryWords(categoryName) {
  return categoryWords[categoryName] || [];
}

// Function to get random word from category
export function getRandomWordFromCategory(categoryName) {
  const words = categoryWords[categoryName];
  if (!words || words.length === 0) {
    return null;
  }
  return words[Math.floor(Math.random() * words.length)];
}
